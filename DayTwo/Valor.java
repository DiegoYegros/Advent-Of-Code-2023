
public class Valor {
    int index;
    int value;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public Valor(int index, int value) {
        this.index = index;
        this.value = value;
    }
}
